			<p><strong>Kategorie</strong> - pevný modul, který se nalézá ve všech konfiguracích. Díky jednoduché správě můžete i velice rozsáhlý webový projekt rozdělit do několika kategorií, které můžou obsahovat strom podkategorií. Získáte tak přehlednou struktůru, celého projektu s možností rozsáhlých nastevní. Veškeré Novinky, Aktuality atd. se pak přiřazují ke kategorii kde se mají zobrazit.</p>
			<p><strong>Aktuality</strong> - modul pro přidávání těch nejjednodušších novinek. Jen prostý text, s možností odkazů, ale velice užitečný a na použití jednoduchý nástroj.</p>
			<p><strong>Novinky</strong> - tento modul plně využívá všech možností redakčního systému a je tak jeho páteřním modulem. Pomocí Novinek lze vytvořit plnohodnotný článek se všemi možnými vlastnostmi. Tento modul muže využívat vlastnosti dalších modulů jejichž popis najdete níže. Můžete si nastavit několik typů zobrazení, od kdy do kdy se má "novinka" zobrazit, jak se má chovat, zda má mít kapitoly či fungovat jako samostatná stránka či podsekce.</p>
			<p><strong>Veřejné Novinky</strong> - díky tomuto modulu můžete zpřístupnit přidávání článků i jinak neprověřeným autorům. Po přidání článku, je třeba článek ještě publikovat což může jen redaktor s příslušným oprávnění. Do té doby je článek viditelný jen v systému, ale na vašich www stránkách se zobrazí právě až po schválení</p>
			<p><strong>Diskuze</strong> - lze využívat samostatně, nebo ve spojitosti právě s Novinkami. Umožní tak čtenářům reagovat na daný článek což vede k větší atraktivitě a kvalitě článků.</p>
			<p><strong>Ankety</strong> - pomocí tohoto modulu můžete k článkům přidávat hodnocení či získávat odpovědi na různé otázky. Použití tohoto modulu není omezeno jen na články a je jen na vás kam se ankety umístí a jaký bude jejich obsah.</p>
			<p><strong>Kalendář</strong> - v tomto modulu si můžete naplánovat či zapsat důležité situace. Je to velice užitečný modul pro spolupráci více osob na projektu. Pomáhá nezapomenout na dokončení úloh.</p>
			<p><strong>Download</strong> - pomocí tohoto modulu můžete snadno přidávat na své stránky soubory i s popisem a mnoha užitečnými informacemi o přidávaném programu programu.</p>
			<p><strong>Clan modul</strong> - jeden z moduú určená herní komunitě lidí, kteří chtějí zobrazovat výsledky svých klání na svých stránkách. Jako všechny moduly je i tento propracován do mnoha detailů a je velice jednoduchý pro použití.</p>
			<p><strong>RSS modul</strong> - jak jiř název tohoto modulu napovídá, jedná se o populární rošíření stránek o zasílání novinek na vyžádání. Nové informace tak vači čtenáři-zákazníci budou dostávat pravidelně až na svůj počítač.</p>
			<p><strong>Autobazar</strong> - modul, který umožní kompletně spravovat katalog aut v autobazaru a jejich publikaci na internetu. Součástí je samozřejmě i vyhledávání automobilů dle zadaných kritérií. Každý zadaný automobil zde má svou kartu s podrobnostmi.</p>
			<p><strong>Partneři</strong> - s tímto modulem můžete na své stránky pohodlně umisťovat textové i grafické odkazy na spřátelené stránky či služby. Součástí je samozřejmě i statistika kliknutí na tyto odkazy.</p>
			<p><strong>Statistiky</strong> - za pomicí tohoto modulu můžete sledovat mnoho kritérií od návštěvnosti vašich stránek, s mnoha položkami, které můžete sledovat, jako je např. možnost vidět, kdo na vaše stránky přistupoval, jaké prostředky k tomu použil a např. kdy se na vaše stránky podíval, přes provoz redakčního systému po čtenost vašich článků.</p>
